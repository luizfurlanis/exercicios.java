import java.util.Scanner;
import java.io.IOException;

public class SentençaDançante {
    public static void main(String[] args) throws IOException{
        Scanner teclado = new Scanner(System.in);
        String sentenca = teclado.next();
        int j = 0;
        for(int i = 0; i < 50; i++)
            if(sentenca[j] = null){ 
                j++; 
            }

        teclado.close();
    }
}
